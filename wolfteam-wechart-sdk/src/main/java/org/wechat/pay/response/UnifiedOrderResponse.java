package org.wechat.pay.response;

import org.wechat.pay.request.BasicPayParams;

/**
 * 下单时 return_code为SUCCESS时返回的对象
 * 
 * @author Andy
 * 
 */
public class UnifiedOrderResponse extends BasicPayParams implements
		java.io.Serializable {

	private String device_info; // 设备号
	private String result_code; // 业务返回码
	private String err_code; // 错误代码
	private String err_code_des; // 错误代码描述
	private String return_code;
	private String trade_type;
	private String prepay_id;
	private String code_url;
	private String return_msg;
	

	public String getReturn_msg() {
		return return_msg;
	}

	public void setReturn_msg(String return_msg) {
		this.return_msg = return_msg;
	}

	public String getDevice_info() {
		return device_info;
	}

	public void setDevice_info(String device_info) {
		this.device_info = device_info;
	}

	public String getResult_code() {
		return result_code;
	}

	public void setResult_code(String result_code) {
		this.result_code = result_code;
	}

	public String getErr_code() {
		return err_code;
	}

	public void setErr_code(String err_code) {
		this.err_code = err_code;
	}

	public String getErr_code_des() {
		return err_code_des;
	}

	public void setErr_code_des(String err_code_des) {
		this.err_code_des = err_code_des;
	}

	public String getReturn_code() {
		return return_code;
	}

	public void setReturn_code(String return_code) {
		this.return_code = return_code;
	}

	public String getTrade_type() {
		return trade_type;
	}

	public void setTrade_type(String trade_type) {
		this.trade_type = trade_type;
	}

	public String getPrepay_id() {
		return prepay_id;
	}

	public void setPrepay_id(String prepay_id) {
		this.prepay_id = prepay_id;
	}

	public String getCode_url() {
		return code_url;
	}

	public void setCode_url(String code_url) {
		this.code_url = code_url;
	}
}
